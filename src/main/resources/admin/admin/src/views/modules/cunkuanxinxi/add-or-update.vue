					{ validator: validateIntNumber, trigger: 'blur' },
				],
				cunkuanriqi: [
				],
				zhanghao: [
				],
			}
		};
	},
	props: ["parent"],
	computed: {



	},
	created() {
		this.ruleForm.cunkuanriqi = this.getCurDate()
	},
	methods: {
		
		// 下载
		download(file){
			window.open(`${file}`)
		},
		// 初始化
		init(id,type) {
			if (id) {
				this.id = id;
				this.type = type;
			}
			if(this.type=='info'||this.type=='else'){
				this.info(id);
			}else if(this.type=='logistics'){
				this.logistics=false;
				this.info(id);
			}else if(this.type=='cross'){
				var obj = this.$storage.getObj('crossObj');
				for (var o in obj){
						if(o=='kahao'){
							this.ruleForm.kahao = obj[o];
							this.ro.kahao = true;
							continue;
						}
						if(o=='yinxingmingcheng'){
							this.ruleForm.yinxingmingcheng = obj[o];
							this.ro.yinxingmingcheng = true;
							continue;
						}
						if(o=='kaxing'){
							this.ruleForm.kaxing = obj[o];
							this.ro.kaxing = true;
							continue;
						}
						if(o=='yonghuming'){
							this.ruleForm.yonghuming = obj[o];
							this.ro.yonghuming = true;
							continue;
						}
						if(o=='xingming'){
							this.ruleForm.xingming = obj[o];
							this.ro.xingming = true;
							continue;
						}
						if(o=='yue'){
							this.ruleForm.yue = obj[o];
							this.ro.yue = true;
							continue;
						}
						if(o=='cunkuanriqi'){
							this.ruleForm.cunkuanriqi = obj[o];
							this.ro.cunkuanriqi = true;
							continue;
						}
						if(o=='zhanghao'){
							this.ruleForm.zhanghao = obj[o];
							this.ro.zhanghao = true;
							continue;
						}
				}
				





				this.ruleForm.yue = 0
				this.ro.yue = false;



			}
			
			
